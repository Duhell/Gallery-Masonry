const GALLERY_CONTAINER = document.querySelector(".gallery-container");
const SELECT = document.querySelector("select");

// Default number of columns
let columns = 4; 

/* ============= ~ EVENTS ~ ============= */

window.addEventListener("DOMContentLoaded", () => render());

SELECT.addEventListener("change", (e) => {
  columns = +e.target.value;
  return render();
});

/* =========== ~ END EVENTS ~ ========== */


function render() {
    
    // Remove first all the contents before rerender
    GALLERY_CONTAINER.replaceChildren();

    // Create fixed columns first
    const columnDivs = Array.from({ length: columns }, () => {
        const div = document.createElement("div");
        div.className = "column flex flex-col space-y-3";
        GALLERY_CONTAINER.appendChild(div);
        return div;
    });

    // Distribute photos among columns
    photos.forEach((photo, index) => {
        const img = document.createElement("img");
        img.src = photo;
        img.className = "rounded-lg object-cover";
        img.setAttribute("loading", "lazy");
        img.setAttribute("alt", `photo-${index}`);
        columnDivs[index % columns].appendChild(img);
    });
}
